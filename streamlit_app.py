import streamlit as st
import sys

def main():
    # 画面のタイトル
    st.title('Streamlit テキスト入力デモ')

    # ------------------------------------
    # よくある質問 (FAQ) のセクション
    # ------------------------------------
    st.subheader('よくある質問 (FAQ)')
    st.write("""
    1. **Q:** このアプリは何ができますか？
       **A:** ユーザーのテキスト入力を受け付け、表示することができます。
    
    2. **Q:** どのように入力すればよいですか？
       **A:** 下にある白いボックス（テキストエリア）に自由に入力してください。

    3. **Q:** 入力した内容はどこに保存されますか？
       **A:** このデモアプリでは、入力内容はサーバーには保存されず、画面上に一時的に表示されるだけです。
    """)
    
    st.markdown("---") # 区切り線

    # ------------------------------------
    # ユーザーのテキスト入力エリア
    # ------------------------------------
    st.subheader('ご質問・コメントをご入力ください')
    
    # ユーザーが複数行のテキストを入力できるエリア
    user_input = st.text_area(
        label="こちらにテキストを入力してください:",
        height=200, # テキストエリアの高さを設定
        placeholder="例：この機能について詳しく教えてください。"
    )

    # ユーザーがテキストを入力した後の表示
    if st.button('送信'):
        if user_input:
            st.success('ご入力ありがとうございます！')
            st.write('**あなたが入力した内容:**')
            st.code(user_input, language='') # 入力内容をコードブロックで表示
        else:
            st.warning('テキストが入力されていません。')

if __name__ == '__main__':
    main()
